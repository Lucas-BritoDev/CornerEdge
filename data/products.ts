// Helper para pegar categoria de um produto
export const getCategoryForProduct = (productName: string): string => {
    const lowerName = productName.toLowerCase();

    // Busca exata ou parcial nas listas
    for (const cat of CATEGORIES) {
        if (cat.products.some(p => p.name.toLowerCase() === lowerName || lowerName.includes(p.name.toLowerCase()))) {
            return cat.id;
        }
        // Busca fuzzy nas keywords se houver
        if (cat.products.some(p => p.keywords?.some(k => lowerName.includes(k)))) {
            return cat.id;
        }
    }

    return 'outros'; // Fallback
};

export const getProductsByCategory = (categoryId: string): ProductSuggestion[] => {
    const category = CATEGORIES.find(c => c.id === categoryId);
    return category?.products || [];
};

export const getAllProducts = (): ProductSuggestion[] => {
    return CATEGORIES.flatMap(c => c.products);
};

export const searchProducts = (query: string): (ProductSuggestion & { categoryId: string })[] => {
    if (!query) return [];
    const q = query.toLowerCase();
    const results: (ProductSuggestion & { categoryId: string })[] = [];

    CATEGORIES.forEach(cat => {
        cat.products.forEach(prod => {
            // Priority for exact starts
            if (prod.name.toLowerCase().startsWith(q)) {
                results.push({ ...prod, categoryId: cat.id });
            }
            // Secondary match
            else if (prod.name.toLowerCase().includes(q) || prod.keywords?.some(k => k.includes(q))) {
                results.push({ ...prod, categoryId: cat.id });
            }
        });
    });

    // Remove duplicates based on name
    const uniqueResults = results.filter((v, i, a) => a.findIndex(v2 => (v2.name === v.name)) === i);

    return uniqueResults.sort((a, b) => a.name.localeCompare(b.name)).slice(0, 10);
};
